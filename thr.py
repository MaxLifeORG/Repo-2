# Variables
a = 10
b = 3

# Arithmetic Operators
sum_val = a + b        # Addition: 13
diff = a - b           # Subtraction: 7
prod = a * b           # Multiplication: 30
div = a / b            # Division: 3.333...
floor_div = a // b     # Floor Division: 3 (removes decimal) [1, 7]
mod = a % b            # Modulo: 1 (remainder) [7]
exp = a ** b           # Exponentiation: 1000 (10^3) [2]

print(f"Sum: {sum_val}")
print(f"diff: {diff}")
print(f"prod: {prod}")
print(f"div: {div}")
print(f"floor_div: {floor_div}")
print(f"exp: {exp}")
